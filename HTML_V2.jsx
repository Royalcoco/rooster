// Script JavaScript pour gérer les interactions avec la page
document.addEventListener("DOMContentLoaded", function () {
    const changeFont = document.getElementById("changeFont");
    const changeSize = document.getElementById("changeSize");
    const alignText = document.getElementById("alignText");

    changeFont.addEventListener("click", function () {
        changeFont.style.fontFamily = "Courier New, monospace";
    });

    changeSize.addEventListener("click", function () {
        changeSize.style.fontSize = "24px";
    });

    alignText.addEventListener("click", function () {
        alignText.style.textAlign = "center";
    });
});

